package com.meme.im.pojo;

public class MemeMovieUpdate {
	private Long movieId;
	private String movieName;//电影名
	private String movieSynopsis;//内容简介
	private String movieCover;//封面地址
	private String movieAddr;//视频地址
	private String account;//会员账号
	public Long getMovieId() {
		return movieId;
	}
	public void setMovieId(Long movieId) {
		this.movieId = movieId;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getMovieSynopsis() {
		return movieSynopsis;
	}
	public void setMovieSynopsis(String movieSynopsis) {
		this.movieSynopsis = movieSynopsis;
	}
	public String getMovieCover() {
		return movieCover;
	}
	public void setMovieCover(String movieCover) {
		this.movieCover = movieCover;
	}
	public String getMovieAddr() {
		return movieAddr;
	}
	public void setMovieAddr(String movieAddr) {
		this.movieAddr = movieAddr;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public MemeMovieUpdate(Long movieId, String movieName, String movieSynopsis, String movieCover, String movieAddr,
			String account) {
		super();
		this.movieId = movieId;
		this.movieName = movieName;
		this.movieSynopsis = movieSynopsis;
		this.movieCover = movieCover;
		this.movieAddr = movieAddr;
		this.account = account;
	}
	public MemeMovieUpdate() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "MemeMovieUpdate [movieId=" + movieId + ", movieName=" + movieName + ", movieSynopsis=" + movieSynopsis
				+ ", movieCover=" + movieCover + ", movieAddr=" + movieAddr + ", account=" + account + "]";
	}
	
}
