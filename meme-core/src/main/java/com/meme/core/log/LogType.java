package com.meme.core.log;

/**
 * 日志类型
 * @author hzl
 *
 */
public enum LogType {

	SEARCH("查询"), DELETE("删除"), ADD("增加"), UPDATE("更新"),SAVE_OR_UPDATE("编辑"),EXCEPTION("异常"),OTHERS("其他");
	private String type;

	LogType(String type) {
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
